"""1. Escribe un programa que lea de la entrada estandar dos números y muestre
en la salida estándar su suma, resta, multiplicación y división."""

num1 = float(input('Digite un numero: '))
num2 = float(input('Digite otro numero: '))

print('La suma es: ', num1+num2)
print('La resta es: ', num1-num2)
print('La multiplicación es: ', num1*num2)
print('La división es: ', num1/num2)
